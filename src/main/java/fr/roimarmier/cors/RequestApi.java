package fr.roimarmier.cors;

public class RequestApi {
	private String url;
	private String apikey;
	
	public RequestApi() {
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getApikey() {
		return apikey;
	}

	public void setApikey(String apikey) {
		this.apikey = apikey;
	}
	
	
}
